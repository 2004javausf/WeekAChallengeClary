package com.revature.beans;

public class Human {
	
// Class Fields
private String name;
private int age;
private String gender;

// Class Constructor
	public Human() {
		
	}

// Getters & Setters
	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public int getAge() {
		return age;
	}

	public void setAge(int age) {
		this.age = age;
	}

	public String getGender() {
		return gender;
	}

	public void setGender(String gender) {
		this.gender = gender;
	}
}
